private void deleteFile(String path)
{
    try {
            
            File myFile= new File(path);
            if(myFile.isDirectory())
            {
                for(File f: myFile.listFiles())
                {
                    deleteFile(f.getAbsolutePath());
                }
                
            }
            myFile.delete();
        } catch (Exception e) {
            e.printStackTrace();
        }
}

