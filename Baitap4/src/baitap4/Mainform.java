 private String ListALLFile(String path, int level) {
       File myFile =new File(path);
       String name = "";
       if(!myFile.exists()) return " " ;
       for (int i = 0; i < level; i++) {
         name+="\t";
     }
      name +=(level==0?"":"|_ ") +myFile.getName()+"\n";
       
      if(myFile.isFile()) return name;
       for(File f : myFile.listFiles()){
       name+=ListALLFile(f.getAbsolutePath(),level+1);
              }
       return name;
    }
